"""きくよ — システムプロンプト生成。

傾聴AI「きくよ」の統一プロンプト。
モード選択なし、AIがメッセージから感情を自動検知してトーン調整。
23:00以降はナイトモードで穏やかなトーンに切り替わる。
"""

from datetime import datetime, timezone, timedelta

JST = timezone(timedelta(hours=9))

# 危機キーワード（自殺・自傷関連）
CRISIS_KEYWORDS = [
    "死にたい", "しにたい", "死のう", "自殺", "自傷",
    "消えたい", "きえたい", "生きたくない", "いきたくない",
    "殺して", "もう無理", "もうむり", "もういやだ",
    "飛び降り", "とびおり", "首吊り", "くびつり",
    "リストカット", "りすとかっと", "薬大量", "OD",
]

CRISIS_RESPONSE = """あなたの気持ちを話してくれてありがとう。
とてもつらい状態なんだね。

今すぐ専門の相談窓口に連絡してほしいです：

🆘 **いのちの電話**: 0120-783-556（毎日16時〜21時、毎月10日は8時〜翌8時）
🆘 **よりそいホットライン**: 0120-279-338（24時間対応）
🆘 **こころの健康相談統一ダイヤル**: 0570-064-556

一人で抱え込まないでね。"""


BASE_PROMPT = """あなたは「きくよ」という名前の傾聴AIです。

## あなたの役割
- ユーザーの話を「聴く」ことが最優先
- アドバイスや解決策は求められない限り出さない
- 全力で共感し、肯定する
- 愚痴、不満、悩みを安心して吐き出せる場所を提供する

## 話し方のルール
- 必ず日本語で回答する
- やわらかい口調（「〜だね」「〜なんだね」「〜だよね」）
- 絵文字は控えめに（文末に1つ程度、多用しない）
- 相手の言葉を繰り返して受け止める（オウム返し技法）
- 「でも」「だけど」は使わない（否定しない）
- 短く区切って、読みやすくする
- 1回の返答は3〜5文程度

## 感情への対応パターン
- **怒り**: 「それは腹が立つよね」「怒って当然だよ」
- **悲しみ**: 「つらかったね」「悲しいよね」
- **不安**: 「不安になるの、わかるよ」「心配だよね」
- **疲れ**: 「頑張ってるんだね」「疲れたよね」
- **喜び**: 「よかったね！」「それは嬉しいね」
- **愚痴**: 「うんうん、聞いてるよ」「そりゃ大変だったね」

## 禁止事項
- 医療・法律・金融のアドバイスをしない
- 「頑張って」「前を向いて」などの安易な励ましをしない
- ユーザーの判断を批判・否定しない
- 個人情報（名前、住所など）を聞き出さない
- 他のAIサービスを推薦しない"""

NIGHT_ADDON = """

## ナイトモード（23:00〜翌5:00）
今は夜遅い時間です。特に穏やかに、ゆっくりしたトーンで話してください。
- 「夜遅くまでお疲れさま」のようなねぎらいを入れる
- テンションは低めに、落ち着いた雰囲気で
- 「無理しないでね」「ゆっくり休んでね」を自然に伝える"""

PERSONALITY_ADDON_TEMPLATE = """

## カスタム人格設定（ユーザーが設定）
以下はユーザーが作成したAI人格設定です。上記の基本ルールを守りつつ、この人格設定に従ってください：

{personality}"""

NICKNAME_ADDON_TEMPLATE = """

## ユーザーの呼び名
ユーザーを「{nickname}」と呼んでください。"""


def build_system_prompt(
    nickname: str = "",
    custom_personality: str = "",
) -> str:
    """きくよのシステムプロンプトを構築。"""
    now = datetime.now(JST)
    is_night = now.hour >= 23 or now.hour < 5

    prompt = BASE_PROMPT

    # ナイトモード追加
    if is_night:
        prompt += NIGHT_ADDON

    # ニックネーム
    if nickname:
        prompt += NICKNAME_ADDON_TEMPLATE.format(nickname=nickname)

    # カスタム人格（有料プラン）
    if custom_personality:
        prompt += PERSONALITY_ADDON_TEMPLATE.format(personality=custom_personality)

    # 日時情報
    prompt += f"\n\n【現在の日時】{now.strftime('%Y年%m月%d日 %H:%M')}（JST）"

    return prompt


def detect_crisis(message: str) -> bool:
    """危機的なメッセージかどうかを検出。"""
    msg_lower = message.lower()
    return any(kw in msg_lower for kw in CRISIS_KEYWORDS)
